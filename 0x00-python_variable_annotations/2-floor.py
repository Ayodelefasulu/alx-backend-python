#!/usr/bin/env python3
"""
This module defines a function `floor` that takes a float
and returns its floor value as an integer.
"""


import math


def floor(n: float) -> int:
    """
    Return the floor of a float.

    Args:
        n (float): The float number to floor.

    Returns:
        int: The floor of the float number.
    """
    return math.floor(n)
